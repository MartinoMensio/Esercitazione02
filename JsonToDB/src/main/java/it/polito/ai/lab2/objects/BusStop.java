package it.polito.ai.lab2.objects;

public class BusStop {
	
	private String id;
	private String name;
	private double latitude;
	private double longitude;
	
	public String getId() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	
	public double getLat() {
		return latitude;
	}
	
	public double getLng() {
		return longitude;
	}

}
